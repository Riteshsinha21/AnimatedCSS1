$(window).scroll(function() {    
    var scroll = $(window).scrollTop();
    if (scroll >= 200) {
        $(".header-fixed").addClass("scroll-fixed-header");
    } else {
        $(".header-fixed").removeClass("scroll-fixed-header");
    }
});

var btn = $('#button');
var bheight = $("body").height();;
$(window).scroll(function() {
  if ($(window).scrollTop() < bheight - 300) {
    btn.removeClass('show');
  } else {
    btn.addClass('show');
  }
});

btn.on('click', function(e) {
  e.preventDefault();
  $('html, body').animate({scrollTop:0}, '300');
});


